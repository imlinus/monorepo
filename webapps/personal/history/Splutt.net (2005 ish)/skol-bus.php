- Ringa in ett bombhot. Skolpersonalen är<br>
skyldiga att evakuera hela skolan tills de har hittat bomben (eller
snarare INTE hittat bomben).<br> Göm gärna en bombatrapp var som helst utom i
ditt eget skåp.<br><br>

- Fixa lite rent kalium eller natrium och spola ner i skolans toalett.<br>
Branden nere i avloppet får gamla avloppsrester att lossna och stinker<br>
följaktligen upp hela skolan. En trevlig effekt ger även kalciumkarbid.<br>
Vid kontakt med vatten bildas en stor mängd etyn, varvid<br>
avloppsledningarna kan brista av trycket. Gasen är även mycket brännbar,<br>
så dumpa i lite karbid i en toa, och lägg en tänd cigg på kanten. När<br>
gasen når upp över kanten, antänds den och det blir ett fint eldklot.<br><br>

- Häll något intressant ämne i matsalens juice. Pepparmyntsolja, salt,<br>
karamellfärg, laxermedel osv... Apelsinjuice i mjölken (eller tvärtom)<br>
ger sega klumpar i drickat. Vilken svag syra som helst ger sega klumpar i
mjölk.<br><br>

- Fyll igen mellanrummet mellan skåpet och ramen med tätningsmassa. Lämna<br>
ett litet hål längst upp. Ta en tratt och/eller en slang och fyll skåpet<br>
med vatten, och täta till slut igen skåpet helt. Överraskning! Det blir<br>
bäst om du tar sådan tätningsmassa som tätar för vatten, men som går att<br>
slita loss om man tar i lite. När någon öppnar skåpet...<br><br>

- Lägg en rökbomb i korridoren.<br><br>

- Lägg en glödlampa ovanpå toalettdörren, när någon öppnar, faller den ner<br>
och exploderar.<br><br>

- Sno klassboken. Släng den, eller ändra alla anteckningarna i den. Bland<br>
klassfacken efter/före skoltid: Blanda alla klassers klassböcker.<br><br>

- Ändra passworden i datasalen. Ändra i AUTOEXEC.BAT. Lägg in porrbilder på<br>
hårddisken som visas vid startup. Sno expansionskorten i datorerna. Sno<br>
mus-kulorna. Lägg även in ett non-destruktivt virus som dock muckar<br>
hemskt mycket på ett kul sätt: exv. PC-Prankster, Lovedos, Cookie.<br><br>

- Bli vän med kontoristerna; be dem sedan ändra dina betyg i skolans<br><br>
betygsdator. (Eller gör det själv!!)<br>

- Rita och skriv på skåpen, väggarna och fasaden att rektorn är fascist.<br><br><br

- Om någon har ett kombinationslås till sitt skåp, ta fram en spegel, titta<br>
i den över axeln, så ser du vilken kombination personen har. Lägg in<br>
rutten frukt i skåpet på fredagseftermiddagen... Du kan också måla över<br>
alla siffrorna med tipp-ex eller tjockt bläck. Då kan man inte öppna.<br><br>

- Häll ut såpa eller flytande tvål i korridoren. Särskilt om det regnar ute<br>
och folk är fuktiga under skorna när de kommer in. Dessa ämnen funkar bra<br>
på ledstängerna i trapphusen också. Blanda såpa med starkt luktande<br>
parfym, så får den stackare som håller i ledstången lukta illa resten av<br>
dan...<br><br>

- Lägg en stinkbomb i korridoren.<br><br>

- Hitta två skåp med likadana hänglås. Dyrka upp båda och byt lås mellan de<br>
båda skåpen. Låsen MIBA, OME och GUARD dyrkar du lätt upp med ett borst<br>
från sopbilen (oslagbar dyrk i förhållande till lättåtkomlighet).<br><br>

- Har skolan sprinklers eller direktkopplat larm till brandkåren, stoppa in<br>
en cigarett i branddetektorerna.<br><br>

- Limma igen alla skolans lås på natten. Alla slipper skolan tills<br>
låssmeden har kommit. Du kan också limma igen vissa skåp. På rasten,<br>
limma igen låset till den salen ni ska vara i nästa lektion. Det fungerar<br>
även bra med tandpetare, tuggummi eller ståltråd.<br><br>

- Fördärva rektorns bil.<br><br>

- Stäng av de små huvudkranarna vid golvet på toaletterna, eller ännu<br>
bättre; den STORA huvudkranen i källaren.<br><br>

- Ta ett hänglås eller slanglås och koppla ihop någons kläder under tiden<br>
som han har gympa. Eftersom han inte får loss sina kläder får han rulta<br>
runt i gymnastikkläder resten av dan. B-)<br><br>

- Koppla in en bandspelare till skolans högtalarsystem. Det kan vara kul<br>
att spela upp rektorns privata telefonsamtal som man tidigare har spelat<br>
in. Du kan också koppla in en mikrofon, sen kan du ropa ut egna speciella<br>
meddelanden.<br><br>

- Haka av klassrummens dörrar. Byt dörrar mellan olika salar så att nya<br>
elever har svårt att hitta. Du kan även ställa tillbaka dörren utan att<br>
haka upp den på gångjärnen igen. När någon öppnar dörren så faller den.<br><br>

- Gladpack över toasitsen, mellan porslinet och ringen, släta ut ordenligt.<br>
Hi Hi! Du kan även smörja in sitsen med kokosfett (halt!).<br><br>

- Har skolan whiteboards, ta bort de speciella whiteboard-pennorna och lägg<br>
dit permanenta spritpennor istället. De går inte att sudda ut.<br><br>

- Mynt: Limma fast enkronor på cafeteriaborden. Eller på korridorgolvet i<br>
rusningen. Eller på rektorns dörr. X-tra bra: I toaletten eller i<br>
urinoaren.<br><br>

- Skriv eller rita något på rullgardinen som overheadbilder visas på.<br>
Favoritcitat "jag hatar overhead!".<br><br>

- Vissa klassrum brukar ha texten "Sätt upp stolarna efter sista lektionen"<br>
skrivna längst uppe i ena hörnet på svarta tavlan. Sudda bara ut ett "s"<br>
och ett "t" i ordet "Sätt". På skylten där det står vilken mat som för<br>
tillfället serveras i matsalen så kan det vissa dagar stå "Pytt i panna".<br>
Denna text brukar ibland få utseendet "Pitt i anna" efter lite suddning.<br>
Rökning Förbjuden blir lätt Rökring Förbjuden, Rök in juden och Pökning<br>
Förbjuden, Rökning För juden. Kom på fler kul kombinationer av din skolas<br>
meddelanden.<br><br>


- Hitta förrådet där diabilder förvaras. Blanda diabilderna eller byt ut en<br>
bild i varje serie mot bilder som du själv har tagit på konstiga saker,<br>
exempelvis rektorn eller en bild på någon som moonar. En liknande sak är<br>
ju att sno videofilmer om franska revolutionen ur skolans förråd, och att<br>
mitt i filmen kopiera in de slemmigaste scenerna ur "Braindead" eller<br>
"Långt ner i halsen". Detta ger intressanta effekter på historia-<br>
lektionen.<br><br>

- Fyll ballonger i kemilabbet med gasol. Släpp ut dem genom fönstret till<br>
de där som står på gården och röker. De gillar säkert att poppa ballonger<br>
med sina cigaretter! ( Gasol + Cigarettglöd = !!! )<br><br>

- När du ändå är i kemisalen, värm upp en krona eller en femma över<br>
bunsenbrännaren. När det ringer ut, släng den i korridoren. Du märker när<br>
någon försöker plocka upp den, kan jag lova...<br><br>

- Vid varje exemplar av den svarta tavlan finns det en tavelsudd. Den kan<br>
man ha kul med: Du kan limma fast den på katedern, eller varför inte på<br>
tavlan? Limma fast en "tricky" på den suddande sidan, när läraren suddar<br>
- KNITT!<br><br>

- Kritor finns det fortfarande i många klassrum. Dessa kan man lacka, och<br>
då går det inte att skriva med dem. Du kan också byta ut de vanliga<br>
kritorna mot godiset "skolkritor", som liknar vanliga kritor, men som<br>
inte går att skriva med, och dessutom är ätbara. (Även den senare<br>
egenskapen kan man nog göra nåt kul med...)<br><br>

- Kan du få tag i s.k. superlim, som håller för ett tons dragkraft, så kan<br>
du ha mycket kul med det. Limma t.ex. fast stolarna i golvet på opassande<br>
ställen, limma fast toalettlocken etc.<br><br>

- Ta någons böcker, och byt ut dem mot någon annans. Du måste även alltid<br>
skriva intressanta meddelanden i din bok om det är en lånebok som du inte<br>
får behålla. Personen som får den året efter kommer att undra.<br><br>

- Kan du få med dig hela klassen: Ingen svarar på tilltal, vad läraren än<br>
säger, eller varför inte nynna en låt i otakt allesammans? Vad ska<br>
läraren göra?<br><br>

- Alla åker ju så klart på ledstängerna i skolan. Här passar det ju bra om<br>
du målar med lite lätt avgnidbar färg, eller hur?<br><br>

- Använd din fantasi! det finns så mycket mer man kan göra på en skola...<br><br>

