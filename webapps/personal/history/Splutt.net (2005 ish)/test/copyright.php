<br>Delar av denna hemsida är copyright-skyddad. Det betyder att 
bara jag som skapare har rätt att använda det skyddade 
materialet. Att ta eller kopiera ett copyright-skyddat verk kan ge upp till 2 års fängelse 
enligt svensk lag (upphovsrättslagen 1960:729 ). Ett verk blir automatiskt 
copyright-skyddat när det uppnått en viss grad självständighet och orginalitet.<br><br>

Nu kanske du undrar: Var går gränsen? Vad får jag kopiera?
Det finns inte någon tydlig gränns, så varken du eller jag vet egentligen,
eftersom det är en jurists jobb att kunna avgöra vart gränsen går.